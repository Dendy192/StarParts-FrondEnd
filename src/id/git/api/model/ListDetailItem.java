package id.git.api.model;

public class ListDetailItem{
	private Double TOTAL;
	private String INVOICE_DATE;
	private Double AMOUNT;
	private String INVOICE_DUE;
	private Double REMAINING_AMOUNT;
	private String INVOICE_NO;
	private String BRAND;
	
	public Double getTOTAL() {
		return TOTAL;
	}
	public void setTOTAL(Double tOTAL) {
		TOTAL = tOTAL;
	}
	public String getINVOICE_DATE() {
		return INVOICE_DATE;
	}
	public void setINVOICE_DATE(String iNVOICE_DATE) {
		INVOICE_DATE = iNVOICE_DATE;
	}
	public Double getAMOUNT() {
		return AMOUNT;
	}
	public void setAMOUNT(Double aMOUNT) {
		AMOUNT = aMOUNT;
	}
	public String getINVOICE_DUE() {
		return INVOICE_DUE;
	}
	public void setINVOICE_DUE(String iNVOICE_DUE) {
		INVOICE_DUE = iNVOICE_DUE;
	}
	public Double getREMAINING_AMOUNT() {
		return REMAINING_AMOUNT;
	}
	public void setREMAINING_AMOUNT(Double rEMAINING_AMOUNT) {
		REMAINING_AMOUNT = rEMAINING_AMOUNT;
	}
	public String getINVOICE_NO() {
		return INVOICE_NO;
	}
	public void setINVOICE_NO(String iNVOICE_NO) {
		INVOICE_NO = iNVOICE_NO;
	}
	public String getBRAND() {
		return BRAND;
	}
	public void setBRAND(String bRAND) {
		BRAND = bRAND;
	}

	
}
